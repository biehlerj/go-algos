package bubblesort

// BubbleSort sorts an array using the bubble sort algorithm:
// https://en.wikipedia.org/wiki/Bubble_sort
func BubbleSort(arr []int) []int {
	for i := 0; i < len(arr)-1; i++ {
		for j := 0; j < len(arr)-i-1; j++ {
			if arr[j] > arr[j+1] {
				arr[j], arr[j+1] = arr[j+1], arr[j]
			}
		}
	}
	return arr
}
